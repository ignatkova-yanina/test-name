<?php
// Указываем путь к файлу
$file = 'data.txt';

// Проверяем, существует ли файл
if (file_exists($file)) {
    // Чтение данных из файла
    $data = file_get_contents($file);
    
    // Выводим содержимое файла
    echo "<h2>Содержимое файла:</h2>";
    echo "<pre>$data</pre>";
} else {
    // Если файл не существует
    echo "Файл не найден!";
}
?>
